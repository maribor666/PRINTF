#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int i;

	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		*(unsigned char*)s = '\0';
		i++;
		s++;
	}
	s -= n;
}
