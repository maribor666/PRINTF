char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int len1;

	len1 = 0;
	i = 0;
	while (s1[len1] != '\0')
		len1++;
	while (s2[i] != '\0')
	{
		s1[len1 + i] = s2[i];
		i++;
	}
	s1[len1 + i] = '\0';
	return (s1);
}
